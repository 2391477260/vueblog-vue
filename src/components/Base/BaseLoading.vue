<template>
  <div class="loading center" v-show="isShow">
    <div class="spinner mt40">
      <div class="rect1"></div>
      <div class="rect2"></div>
      <div class="rect3"></div>
      <div class="rect4"></div>
      <div class="rect5"></div>
    </div>
  </div>
</template>

<script>
export default {
  data: function () {
    return {
      isShow: false
    }
  },
  computed: {
    show: function () {
      return this.$store.state.loading
    }
  },

  watch: {
    show: async function (value) {
      if (value) {
        await new Promise(resolve => {
          setTimeout(() => {
            resolve()
          }, 400)
        })

        this.show && (this.isShow = true)
      } else {
        this.isShow = false
      }
    }
  }
}
</script>

